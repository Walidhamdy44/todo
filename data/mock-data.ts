// =====================================
// Helper function to get dashboard stats
// =====================================

export function getDashboardStats() {
    const today = new Date().toISOString().split('T')[0];

    const tasksToday = mockTasks.filter(
        task => task.deadline === today && task.status !== 'done'
    ).length;

    const activeCourses = mockCourses.filter(
        course => course.status === 'in-progress'
    ).length;

    const readingItems = mockReadingItems.filter(
        item => item.status === 'to-read' || item.status === 'reading'
    ).length;

    const activeGoals = mockGoals.filter(goal => goal.status === 'active');
    const goalsProgress = activeGoals.length > 0
        ? Math.round(activeGoals.reduce((sum, goal) => sum + goal.progress, 0) / activeGoals.length)
        : 0;

    const completedThisWeek = mockTasks.filter(task => task.status === 'done').length;

    return {
        tasksToday,
        activeCourses,
        readingItems,
        goalsProgress,
        completedThisWeek,
        studyStreak: 7,
        productivityScore: 85,
    };
}
